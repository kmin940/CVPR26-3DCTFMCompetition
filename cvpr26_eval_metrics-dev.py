"""
Evaluate metrics from per-sample predictions CSV file.
This script reads the per-sample predictions CSV generated by inference.py
and computes the same metrics to verify reproducibility.
"""

import argparse
import os
import pandas as pd
import torch
import numpy as np
from torchmetrics import (
    Accuracy,
    F1Score,
    AUROC,
    AveragePrecision,
    Recall,
    Specificity,
    MetricCollection,
)
from metrics.balanced_accuracy import BalancedAccuracy


def build_metrics(num_classes, device):
    """Build metric collection matching inference.py"""
    # Alghough our CVPR26 challenge ranking only considers balanced_acc, auroc, we provide the full set of metrics.
    base_metrics = MetricCollection({
        "acc": Accuracy(task="multiclass", num_classes=num_classes),
        "f1": F1Score(task="multiclass", num_classes=num_classes, average="macro"),
        "auroc": AUROC(task="multiclass", num_classes=num_classes),
        "ap": AveragePrecision(task="multiclass", num_classes=num_classes),
        "sensitivity": Recall(task="multiclass", num_classes=num_classes, average="macro"),
        "specificity": Specificity(task="multiclass", num_classes=num_classes, average="macro"),
        "balanced_acc": BalancedAccuracy(num_classes=num_classes, task="multiclass"),
    }).to(device)
    return base_metrics


def main():
    ap = argparse.ArgumentParser(
        description="Evaluate metrics from per-sample predictions CSV for all diseases"
    )
    ap.add_argument(
        "--csv_root",
        type=str,
        default="/home/jma/Documents/cryoSumin/CT_FM/data/embeddings/features_EAO_public_MultiStage",
        help="Root directory containing disease subfolders with results/",
    )
    ap.add_argument(
        "--label_dir",
        type=str,
        default="/home/jma/Documents/cryoSumin/CT_FM/data/raw_data_classify/amos-clf-tr-val/labels",
        help="Directory containing per-disease label CSV files (e.g., adrenal_hyperplasia.csv)",
    )
    ap.add_argument(
        "--pred_csv_name",
        type=str,
        default="val_per_sample_predictions.csv",
        help="Name of the per-sample predictions CSV file inside each disease's results/ folder",
    )
    args = ap.parse_args()

    # Discover disease names from label files
    label_files = sorted([f for f in os.listdir(args.label_dir) if f.endswith('.csv')])
    disease_names = [os.path.splitext(f)[0] for f in label_files]

    device = "cuda" if torch.cuda.is_available() else "cpu"
    all_dfs = []

    metrics_list = []

    for disease_name in disease_names:
        csv_path = os.path.join(args.csv_root, disease_name, "results", args.pred_csv_name)

        if not os.path.exists(csv_path):
            print(f"[SKIP] {disease_name}: {csv_path} not found")
            continue

        print(f"[{disease_name}] Loading {csv_path}")
        df = pd.read_csv(csv_path)

        # Extract labels and predictions
        labels = torch.tensor(df['label'].values).long()
        predictions = torch.tensor(df['prediction'].values).long()

        # Extract logits (columns starting with 'logit_class_')
        logit_cols = [col for col in df.columns if col.startswith('logit_class_')]
        logit_cols = sorted(logit_cols, key=lambda x: int(x.split('_')[-1]))
        logits = torch.tensor(df[logit_cols].values).float()

        # Extract probabilities (columns starting with 'prob_class_')
        prob_cols = [col for col in df.columns if col.startswith('prob_class_')]
        prob_cols = sorted(prob_cols, key=lambda x: int(x.split('_')[-1]))
        probs = torch.tensor(df[prob_cols].values).float()

        num_classes = len(logit_cols)
        print(f"  Samples: {len(df)}, Classes: {num_classes}")

        # Compute metrics for this disease
        metrics = build_metrics(num_classes, device)
        logits_dev = logits.to(device)
        labels_dev = labels.to(device)

        metrics.update(logits_dev, labels_dev)
        computed = metrics.compute()

        computed_cpu = {}
        for k, v in computed.items():
            computed_cpu[k] = v.item() if torch.is_tensor(v) else v

        print("  Metrics:")
        for k, v in computed_cpu.items():
            print(f"    {k}: {v:.4f}")

        computed_cpu['disease_name'] = disease_name
        metrics_list.append(computed_cpu)

        # Add disease_name column
        df['disease_name'] = disease_name

        # Keep only the desired columns
        keep_cols = ['filename', 'label', 'prediction']
        keep_cols += [c for c in df.columns if c.startswith('logit_class_')]
        keep_cols += [c for c in df.columns if c.startswith('prob_class_')]
        keep_cols += ['disease_name']
        all_dfs.append(df[keep_cols])

    if not all_dfs:
        print("No disease results found.")
        return

    # Concatenate all per-sample rows
    df_all = pd.concat(all_dfs, ignore_index=True)

    output_csv = os.path.join(args.csv_root, "predictions.csv")
    df_all.to_csv(output_csv, index=False)

    print(f"\nTotal samples across all diseases: {len(df_all)}")
    print(f"Saved concatenated per-sample predictions to {output_csv}")

    # Compute overall metrics
    metrics_df = pd.DataFrame(metrics_list)
    metrics_df = metrics_df[['disease_name', 'balanced_acc', 'auroc']]
    metrics_df.to_csv(os.path.join(args.csv_root, "metrics.csv"), index=False)
    print(f"Saved metrics to {os.path.join(args.csv_root, 'metrics.csv')}")


if __name__ == "__main__":
    main()
